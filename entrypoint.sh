#!/usr/bin/env bash
set -eu

DATABASE_NAME=${DATABASE_NAME-wordpress}
DATABASE_USER=${DATABASE_USER-root}
DATABASE_PASSWORD=${DATABASE_PASSWORD}
DATABASE_HOST=${DATABASE_HOST-database}
DATABASE_PORT=${DATABASE_PORT-3306}

wait-for-it --timeout=30 "${DATABASE_HOST}:${DATABASE_PORT}" -- install-wp-tests "${DATABASE_NAME}" "${DATABASE_USER}" "${DATABASE_PASSWORD}" "${DATABASE_HOST}:${DATABASE_PORT}" latest
exec "${@}"
